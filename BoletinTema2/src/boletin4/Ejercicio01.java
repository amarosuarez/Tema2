package boletin4;

import java.util.Scanner;

public class Ejercicio01 {

	public static void main(String[] args) {
		// Variables donde se almacenará el contador
		int cont = 1;
		
		// Condición que muestra los números enteros del 1 al 20
		do {
			System.out.println(cont);
			cont++;
		} while (cont <= 20);

	}
	
}
